function maiorIdade(){
    // Implementar lógica para verificar se a pessoa é maior de idade
    
    let maiordeidade = 18;
    
    let idade = parseInt(prompt("Informe a sua idade: "));
    
    if(idade >= maiordeidade){
        alert("Você é maior de idade!");
    }

    else {
        alert("Você não é maior de idade!");
    }


}
